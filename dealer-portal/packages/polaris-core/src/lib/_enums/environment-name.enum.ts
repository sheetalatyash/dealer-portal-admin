/**
 * Enum representing the different environment names.
 */
export enum EnvironmentName {
  /**
   * Local environment - Used in local.
   */
  Local = 'local',

  /**
   * Development environment - e.g. https://home-dt-dev.polarisportal.com/en/
   */
  Development = 'development',

  /**
   * Staging environment - e.g. https://home-dt-stg.polarisportal.com/en/
   */
  Staging = 'staging',

  /**
   * Production environment - e.g. https://home-dt-prod.polarisportal.com/en/
   */
  Production = 'production',
}